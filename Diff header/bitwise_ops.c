#include "bitwise_ops.h"

// Perform Bitwise AND
int bitwise_and(int a, int b) {
    return a & b;
}

// Perform Bitwise OR
int bitwise_or(int a, int b) {
    return a | b;
}

// Perform Bitwise XOR
int bitwise_xor(int a, int b) {
    return a ^ b;
}

// Perform Left Shift
int left_shift(int a, int shift) {
    return a << shift;
}

// Perform Right Shift
int right_shift(int a, int shift) {
    return a >> shift;
}
